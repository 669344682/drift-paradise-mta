local tuningTable = {}

function getVehicleTuningTable(name)
	if name and tuningTable[name] then
		return tuningTable[name]
	else
		outputDebugString(string.format("Warning: no tuning table for \"%s\"", tostring(name)))
		return tuningTable.default
	end
end

